import { Sypo } from '@src/components/atoms';

import useT from '@src/hooks/Locale/useT';

import styles from './FileState.module.scss';
import classNames from 'classnames/bind';

import { FileStatus } from '@tools/types/label';

const cx = classNames.bind(styles);

type Props = {
  type: FileStatus;
};

function FileState({ type }: Props) {
  const { t } = useT();

  const iconRender = () => {
    switch (type) {
      case 0:
        return <NotAssigned />;
      case 1:
        return <Approved />;
      case 2:
        return <InProgress />;
      case 3:
        return <Submitted />;
      case 4:
        return <Rejected />;
    }
  };

  const stringRender = () => {
    switch (type) {
      case 0:
        return t(`component.jobstatus.notassigned`);
      case 1:
        return t(`component.jobstatus.approved`);
      case 2:
        return t(`component.jobstatus.inprogress`);
      case 3:
        return t(`component.jobstatus.submitted`);
      case 4:
        return t(`component.jobstatus.rejected`);
    }
  };

  return (
    <div className={cx('file-state')}>
      {iconRender()}
      <Sypo type='p2' weight='r'>
        {stringRender()}
      </Sypo>
    </div>
  );
}

export type { Props as FileStatePropType };

export default FileState;

